function reverseVehicleHash(hash)
    if type(hash) ~= "string" then
        hash = tostring(hash)
    end

    local name = VEHICLE_HASHES[hash]
    if name ~= nil then
        return name
    end

    Citizen.Trace("Error reversing vehicle hash \"" .. hash .. "\". Maybe it's not been added yet?")
    return "Unknown Vehicle"
end
